import numpy as np
import matplotlib.pyplot as plt
from matplotlib.animation import FuncAnimation
from mpl_toolkits.mplot3d import Axes3D
from IPython.display import HTML

# --- Parameters ---
num_particles = 200                # number of particles in disk
max_radius = 1.5                   # initial outer radius
min_radius = 0.2                   # closest approach to black hole
omega = np.random.uniform(0.1, 0.3, num_particles)  # angular speeds
k = np.random.uniform(0.002, 0.008, num_particles)  # spiral-in rate
colors = plt.cm.plasma(np.linspace(0,1,num_particles))  # color map
z_amp = 0.05 * np.random.uniform(0.5, 1.0, num_particles)  # vertical motion
z_freq = np.random.uniform(0.5, 1.5, num_particles)

# Random initial radii
r0 = np.random.uniform(min_radius, max_radius, num_particles)

# --- Figure setup ---
fig = plt.figure(figsize=(8,8))
ax = fig.add_subplot(111, projection='3d')
ax.set_xlim(-2,2)
ax.set_ylim(-2,2)
ax.set_zlim(-0.5,0.5)
ax.set_facecolor('black')
ax.view_init(elev=30, azim=45)
ax.set_xticks([]); ax.set_yticks([]); ax.set_zticks([])
ax.set_title("3D Accretion Disk around Black Hole", color='white')

# Black hole at center
ax.scatter(0,0,0,color='k',s=120)

# Create particle points
points = []
for i in range(num_particles):
    p, = ax.plot([], [], [], 'o', color=colors[i], markersize=3, alpha=0.8)
    points.append(p)

# Store trails
trail_length = 20
x_trails = np.zeros((num_particles, trail_length))
y_trails = np.zeros((num_particles, trail_length))
z_trails = np.zeros((num_particles, trail_length))
lines = []
for i in range(num_particles):
    line, = ax.plot([], [], [], color=colors[i], alpha=0.4)
    lines.append(line)

# --- Update function ---
def update(frame):
    t = frame
    for i, p in enumerate(points):
        # Spiral radius decays
        r = r0[i] * np.exp(-k[i]*t)
        theta = omega[i]*t
        z = z_amp[i] * np.sin(z_freq[i]*theta)
        x = r * np.cos(theta)
        y = r * np.sin(theta)

        # Update particle
        p.set_data([x], [y])
        p.set_3d_properties([z])

        # Update trails
        x_trails[i] = np.roll(x_trails[i], -1)
        y_trails[i] = np.roll(y_trails[i], -1)
        z_trails[i] = np.roll(z_trails[i], -1)
        x_trails[i,-1] = x
        y_trails[i,-1] = y
        z_trails[i,-1] = z
        lines[i].set_data(x_trails[i], y_trails[i])
        lines[i].set_3d_properties(z_trails[i])

    # Slowly rotate camera
    ax.view_init(elev=30, azim=45 + frame*0.3)
    return points + lines

# --- Animate ---
ani = FuncAnimation(fig, update, frames=800, interval=30, blit=False)
HTML(ani.to_jshtml())

