import numpy as np
import matplotlib.pyplot as plt
from matplotlib.animation import FuncAnimation
from IPython.display import HTML

# --- Constants ---
G = 6.6743e-11
AU = 1.495978707e11
M_sun = 1.989e30

# Planet masses and orbital radii (m)
M_earth, R_earth = 5.9742e24, 1.0 * AU
M_mer, R_mer = 3.285e23, 0.39 * AU
M_ven, R_ven = 4.867e24, 0.72 * AU
M_mar, R_mar = 6.42e23, 1.52 * AU

# Scale factor (to fit in the plot window)
Scale = 250 / AU

# --- Orbital angular speeds (Kepler's law approximation) ---
# omega = sqrt(G M_sun / r^3)
omega_earth = np.sqrt(G * M_sun / R_earth**3)
omega_mer   = np.sqrt(G * M_sun / R_mer**3)
omega_ven   = np.sqrt(G * M_sun / R_ven**3)
omega_mar   = np.sqrt(G * M_sun / R_mar**3)

# --- Figure setup ---
fig, ax = plt.subplots(figsize=(6,6))
ax.set_xlim(-250,250)
ax.set_ylim(-250,250)
ax.set_aspect('equal')
ax.set_title("Solar System Orbits (Scaled)")

# Sun
ax.plot(0, 0, 'yo', markersize=12, label="Sun")

# Static orbit paths (scaled)
theta = np.linspace(0, 2*np.pi, 300)
ax.plot(Scale*R_earth*np.cos(theta), Scale*R_earth*np.sin(theta), 'b--', alpha=0.3)
ax.plot(Scale*R_mer*np.cos(theta),   Scale*R_mer*np.sin(theta),   'k--', alpha=0.3)
ax.plot(Scale*R_ven*np.cos(theta),   Scale*R_ven*np.sin(theta),   'orange', alpha=0.3)
ax.plot(Scale*R_mar*np.cos(theta),   Scale*R_mar*np.sin(theta),   'r--', alpha=0.3)

# Moving planets (dots)
earth, = ax.plot([], [], 'bo', markersize=6, label="Earth")
mer,   = ax.plot([], [], 'ko', markersize=4, label="Mercury")
ven,   = ax.plot([], [], 'yo', markersize=5, label="Venus")
mar,   = ax.plot([], [], 'ro', markersize=5, label="Mars")

ax.legend(loc='upper right')

# --- Update function ---
def update(frame):
    t = frame * 1e4  # time step (arbitrary scaling)

    # Orbital positions (scaled)
    x_e, y_e = Scale * R_earth * np.cos(omega_earth * t), Scale * R_earth * np.sin(omega_earth * t)
    x_mer, y_mer = Scale * R_mer * np.cos(omega_mer * t), Scale * R_mer * np.sin(omega_mer * t)
    x_ven, y_ven = Scale * R_ven * np.cos(omega_ven * t), Scale * R_ven * np.sin(omega_ven * t)
    x_mar, y_mar = Scale * R_mar * np.cos(omega_mar * t), Scale * R_mar * np.sin(omega_mar * t)

    # Update planet positions
    earth.set_data([x_e], [y_e])
    mer.set_data([x_mer], [y_mer])
    ven.set_data([x_ven], [y_ven])
    mar.set_data([x_mar], [y_mar])

    return earth, mer, ven, mar

# --- Animate ---
ani = FuncAnimation(fig, update, frames=500, interval=30, blit=True)
HTML(ani.to_jshtml())

